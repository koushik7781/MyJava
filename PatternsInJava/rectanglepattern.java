import java.util.*;

public class rectanglepattern {
    public static void main(String[] args) {
        Scanner a=new Scanner(System.in);

        for(int i=1;i<=10;i++)
        {
            for(int j=1;j<=20;j++)
            {
                System.out.print("*");
            }
            System.out.println("*");
        }
         a.close();
    }
}
